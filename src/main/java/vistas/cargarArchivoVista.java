    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        
        // Establecer el modo de selecci칩n de archivos (puede ser archivos o directorios)
        fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY); // Solo archivos
        
        // Mostrar el di치logo de selecci칩n de archivos
        int result = fileChooser.showOpenDialog(null);
        
        // Verificar si el usuario seleccion칩 un archivo
        if (result == JFileChooser.APPROVE_OPTION) {
            // Obtener el archivo seleccionado
            File selectedFile = fileChooser.getSelectedFile();
            
            // Obtener la ruta completa del archivo seleccionado
            String filePath = selectedFile.getAbsolutePath();
            cargarArchivo carga = new cargarArchivo();
            ArrayList<String> respuestas = carga.cargarArchivo(filePath, selectedFile.getParent());
            
            this.jTextArea1.setText("");
            for (int i = 0; i < respuestas.size(); i++) {
                String respuesta = respuestas.get(i);
                this.jTextArea1.append(respuesta+"\n");
            }
        } 
    }//GEN-LAST:event_jButton1ActionPerformed

